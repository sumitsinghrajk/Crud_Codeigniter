    public function __construct() {
        parent::__construct();
        // Load necessary models and libraries
        $this->load->model('user_model');
    }

    public function delete($user_id) {
        // Check if user has the necessary permissions 
        if (!$this->ion_auth->is_admin()) {
            // Handle unauthorized access
            show_error('Unauthorized Access', 403);
            return;
        }
    
        // Delete user using the model
        $result = $this->user_model->delete_user($user_id);
    
        // Check the result and redirect accordingly
        if ($result) {
            $this->session->set_flashdata('success', 'User deleted successfully.');
            redirect('auth/index'); // Redirect to user list page
        } else {
            // Failed to delete user, handle accordingly
            $this->session->set_flashdata('failure', 'User not found.');
            show_error('Failed to delete user', 500);
        }
    }
    
    
